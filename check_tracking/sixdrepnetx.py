    def plot_angles(self, img: np.ndarray, euler_rad: np.ndarray, td: np.ndarray):
        euler = euler_rad*180/np.pi
        self.plot(img, euler[:,0], euler[:,1],euler[:,2], td[:,0], td[:,1])

    def plot(self, img: np.ndarray,
             pitch: np.ndarray, yaw: np.ndarray, roll: np.ndarray,
             tdx: Optional[np.ndarray]=None, tdy: Optional[np.ndarray]=None,
             size: int=30):
        """
        Prints the person's name and age.
        If the argument 'additional' is passed, then it is appended after the main info.

        WARNING: 'pitch, yaw, roll', SAME ORDER of the angles generated by 'predict()'
            The original implementation of 'draw_axes' use a DIFFERENT ORDER of the angles!!!!!

        Parameters
        ----------
        img : array
            Target image to be drawn on
        pitch: int
            pitch rotation
        yaw : int
            yaw rotation
        roll: int
            roll rotation
        tdx : int , optional
            shift on x axis
        tdy : int , optional
            shift on y axis

        Returns
        -------
        img : array
        """

        pitch = pitch * np.pi / 180
        yaw = -(yaw * np.pi / 180)
        roll = roll * np.pi / 180

        for i in range(yaw.shape[0]):
            pitch_i = float(pitch[i])
            yaw_i = float(yaw[i])
            roll_i = float(roll[i])

            if tdx is not None and tdy is not None:
                tdx_i = tdx[i]
                tdy_i = tdy[i]
            else:
                height, width = img.shape[:2]
                tdx_i = int(width / 2)
                tdy_i = int(height / 2)

            # X-Axis pointing to right. drawn in red
            x1 = size * (cos(yaw_i) * cos(roll_i)) + tdx_i
            y1 = size * (cos(pitch_i) * sin(roll_i) + cos(roll_i) * sin(pitch_i) * sin(yaw_i)) + tdy_i

            # Y-Axis | drawn in green
            #        v
            x2 = size * (-cos(yaw_i) * sin(roll_i)) + tdx_i
            y2 = size * (cos(pitch_i) * cos(roll_i) - sin(pitch_i) * sin(yaw_i) * sin(roll_i)) + tdy_i

            # Z-Axis (out of the screen) drawn in blue
            x3 = size * (sin(yaw_i)) + tdx_i
            y3 = size * (-cos(yaw_i) * sin(pitch_i)) + tdy_i

            cv2.line(img, (int(tdx_i), int(tdy_i)), (int(x1), int(y1)), (0, 0, 255), 4)
            cv2.line(img, (int(tdx_i), int(tdy_i)), (int(x2), int(y2)), (0, 255, 0), 4)
            cv2.line(img, (int(tdx_i), int(tdy_i)), (int(x3), int(y3)), (255, 0, 0), 4)
        # end
        return img
    # end
