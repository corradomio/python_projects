def iclip(x: float, xmin: int, xmax: int)-> int:
    if x < xmin: x = xmin
    if x > xmax: x = xmax
    return int(x)


class SixDRepNetMulti(SixDRepNet):
    def __init__(self, gpu_id : int=0, dict_path: str=''):
        super().__init__(gpu_id, dict_path)


    def _predict(self, img):
        """
        Predicts the persons head pose and returning it in euler angles.

        Parameters
        ----------
        img : array
            Face crop to be predicted

        Returns
        -------
        pitch, yaw, roll
        """
        if isinstance(img, np.ndarray):
            try:
                img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
                img = Image.fromarray(img)
            except:
                pass
        img = self.transformations(img)

        img = torch.Tensor(img[None, :])

        if self.gpu != -1:
            img = img.cuda(self.gpu)

        pred = self.model(img)

        euler_rad = utils.compute_euler_angles_from_rotation_matrices(pred)# * 180 / np.pi
        # p = euler[:, 0].cpu().detach().numpy()
        # y = euler[:, 1].cpu().detach().numpy()
        # r = euler[:, 2].cpu().detach().numpy()
        # return p, y, r
        return euler_rad.cpu().detach().numpy()
    # end

    def predict_angles(self, frame, face_boxes):
        # frame: (h:240, w:320, c:3)
        h, w, c = frame.shape
        n_faces = len(face_boxes)
        euler_rad = np.zeros((n_faces, 3))
        td = np.zeros((n_faces, 2), dtype=float)
        for i in range(n_faces):
            tx, ty, bx, by, prob = face_boxes[i]

            face_frame = frame[iclip(ty, 0, h):iclip(by, 0, h), iclip(tx, 0, w):iclip(bx, 0, w)]

            euler_rad[i:i + 1] = self._predict(face_frame)
            td[i, 0] = (tx + bx) / 2
            td[i, 1] = (ty + by) / 2

        # euler = euler_rad * 180 / np.pi
        return euler_rad, td
    # end

    def predict(self, frame, face_boxes=[]):
        euler_rad, td = self.predict_angles(frame, face_boxes)

        euler = euler_rad * 180 / np.pi

        return euler[:, 0], euler[:, 1], euler[:, 2], td[:, 0], td[:, 1]
    # end

